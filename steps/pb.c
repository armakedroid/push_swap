#include "../push_swap.h"

void	pb(t_m **b, t_m **a)
{
	t_m	*b1;
	t_m	*a1;
	t_m	*a2;

	if (!(*a))
		return ;
	b1 = *b;
	a1 = *a;
	a2 = (*a)-> next;
	if (*b)
		b1->prev = a1;
	a1->next = b1;
	if (a2)
		a2->prev = NULL;
	*a = a2;
	(*b) = a1;
	write(1, "pb\n", 3);
}
